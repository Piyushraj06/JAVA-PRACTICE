public class swap {
    public static void main(String args []){
        int a, b, temp;
        a=14;
        b=16;
        System.out.println("Before swapping : a, b = "+ a +", "+  b);
         temp = a;
         a=b;
         b=temp;
         System.out.println("after swapping : a, b = "+a+", "+  b);
    }
    
}
